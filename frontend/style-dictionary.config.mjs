import StyleDictionary from 'style-dictionary';

const generatedHeader = 'Generated by Style Dictionary from src/design-system/tokens.json.';
const selectorOrder = [':root', '.dark', '.theme'];
const registryFamilyNames = {
  breakpoint: 'axisBreakpointTokens',
  gradient: 'axisGradientTokens',
  motion: 'axisMotionTokens',
  radius: 'axisRadiusTokens',
  semanticColor: 'axisSemanticColorTokens',
  shadow: 'axisShadowTokens',
  sizing: 'axisSizingTokens',
  spacing: 'axisSpacingTokens',
  typography: 'axisTypographyTokens',
};
const tailwindRadiusTokens = {
  lg: 'var(--radius)',
  md: 'calc(var(--radius) - 2px)',
  sm: 'calc(var(--radius) - 4px)',
};

function axisExtension(token) {
  return token.$extensions?.axis ?? token.original?.$extensions?.axis ?? {};
}

function tokenValue(token) {
  return token.$value ?? token.value ?? token.original?.$value;
}

function axisTokens(dictionary) {
  return dictionary.allTokens.filter((token) => axisExtension(token).cssVariable);
}

function compareBySelector(orderByToken) {
  return (left, right) => {
    const leftSelector = selectorOrder.indexOf(axisExtension(left).selector);
    const rightSelector = selectorOrder.indexOf(axisExtension(right).selector);

    if (leftSelector !== rightSelector) {
      return leftSelector - rightSelector;
    }

    return orderByToken.get(left) - orderByToken.get(right);
  };
}

function groupedBySelector(dictionary) {
  const groups = new Map(selectorOrder.map((selector) => [selector, []]));
  const orderedTokens = axisTokens(dictionary);

  const orderByToken = new Map(orderedTokens.map((token, index) => [token, index]));

  for (const token of orderedTokens.sort(compareBySelector(orderByToken))) {
    const selector = axisExtension(token).selector;

    if (!groups.has(selector)) {
      groups.set(selector, []);
    }

    groups.get(selector).push(token);
  }

  return [...groups.entries()].filter(([, tokens]) => tokens.length > 0);
}

function cssVariables({ dictionary }) {
  const blocks = groupedBySelector(dictionary).map(([selector, tokens]) => {
    const lines = tokens.map((token) => {
      const extension = axisExtension(token);
      return `    ${extension.cssVariable}: ${cssValue(tokenValue(token))};`;
    });

    if (selector === ':root') {
      lines.unshift('    /* Axis control plane */');
    }

    return `  ${selector} {\n${lines.join('\n')}\n  }`;
  });

  return `@layer base {\n${blocks.join('\n\n')}\n}\n`;
}

function splitTopLevelArguments(value) {
  const parts = [];
  let depth = 0;
  let start = 0;

  for (let index = 0; index < value.length; index += 1) {
    const character = value[index];

    if (character === '(') {
      depth += 1;
    } else if (character === ')') {
      depth -= 1;
    } else if (character === ',' && depth === 0) {
      parts.push(value.slice(start, index).trim());
      start = index + 1;
    }
  }

  parts.push(value.slice(start).trim());
  return parts;
}

function cssValue(value) {
  const rawValue = String(value);
  const gradientPrefix = 'linear-gradient(';

  if (!rawValue.startsWith(gradientPrefix) || !rawValue.endsWith(')') || rawValue.length <= 100) {
    return rawValue;
  }

  const parts = splitTopLevelArguments(rawValue.slice(gradientPrefix.length, -1));

  if (parts.length < 2) {
    return rawValue;
  }

  const lines = parts.map((part, index) => {
    const suffix = index === parts.length - 1 ? '' : ',';
    return `      ${part}${suffix}`;
  });

  return `linear-gradient(\n${lines.join('\n')}\n    )`;
}

function quote(value) {
  return `'${String(value).replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`;
}

function objectKey(key) {
  if (/^(0|[1-9]\d*)$/.test(key)) {
    return key;
  }

  return /^[A-Za-z_$][A-Za-z0-9_$]*$/.test(key) ? key : quote(key);
}

function literal(value, depth = 0) {
  const indent = '  '.repeat(depth);
  const childIndent = '  '.repeat(depth + 1);

  if (typeof value === 'string') {
    return quote(value);
  }

  if (Array.isArray(value)) {
    if (value.length === 0) {
      return '[]';
    }

    if (value.length === 1 && typeof value[0] === 'string') {
      return `[${quote(value[0])}]`;
    }

    return `[\n${value.map((item) => `${childIndent}${literal(item, depth + 1)},`).join('\n')}\n${indent}]`;
  }

  if (typeof value === 'object' && value !== null) {
    const entries = Object.entries(value);

    if (entries.length === 0) {
      return '{}';
    }

    return `{\n${entries
      .map(([key, item]) => `${childIndent}${objectKey(key)}: ${literal(item, depth + 1)},`)
      .join('\n')}\n${indent}}`;
  }

  return String(value);
}

function setPath(root, path, value) {
  let current = root;

  for (const segment of path.slice(0, -1)) {
    current[segment] ??= {};
    current = current[segment];
  }

  current[path[path.length - 1]] = value;
}

function registryTokens(dictionary, family) {
  return axisTokens(dictionary)
    .filter((token) => axisExtension(token).registryFamily === family)
    .map((token) => axisExtension(token).cssVariable);
}

function tailwindColorTokens(dictionary, mode) {
  const root = {};

  for (const token of axisTokens(dictionary)) {
    const extension = axisExtension(token);

    if (!extension.tailwindColorPath) {
      continue;
    }

    const value =
      mode === 'css-variable' ? extension.cssVariable : `hsl(var(${extension.cssVariable}))`;
    setPath(root, extension.tailwindColorPath, value);
  }

  return root;
}

function tailwindShadowTokens(dictionary) {
  const root = {};

  for (const token of axisTokens(dictionary)) {
    const extension = axisExtension(token);

    if (!extension.tailwindShadowName) {
      continue;
    }

    root[extension.tailwindShadowName] = `var(${extension.cssVariable})`;
  }

  return root;
}

function tailwindBackgroundImageTokens(dictionary) {
  const root = {};

  for (const token of axisTokens(dictionary)) {
    const extension = axisExtension(token);

    if (!extension.tailwindBackgroundImageName) {
      continue;
    }

    root[extension.tailwindBackgroundImageName] = `var(${extension.cssVariable})`;
  }

  return root;
}

function typescriptRegistry({ dictionary }) {
  const sections = [`// ${generatedHeader}`, '// Do not edit this file directly.'];

  for (const [family, exportName] of Object.entries(registryFamilyNames)) {
    sections.push(
      `export const ${exportName} = ${literal(registryTokens(dictionary, family))} as const;`,
    );
  }

  sections.push(
    `export const axisTailwindColorTokens = ${literal(
      tailwindColorTokens(dictionary, 'css-variable'),
    )} as const;`,
  );
  sections.push(
    `export const axisTailwindRadiusTokens = ${literal(tailwindRadiusTokens)} as const;`,
  );
  sections.push(
    `export const axisTailwindShadowTokens = ${literal(tailwindShadowTokens(dictionary))} as const;`,
  );
  sections.push(
    `export const axisTailwindBackgroundImageTokens = ${literal(
      tailwindBackgroundImageTokens(dictionary),
    )} as const;`,
  );

  return `${sections.join('\n\n')}\n`;
}

function tailwindTokenMap({ dictionary }) {
  const sections = [
    `// ${generatedHeader}`,
    '// Do not edit this file directly.',
    `export const axisTailwindColorTokens = ${literal(tailwindColorTokens(dictionary, 'tailwind'))};`,
    `export const axisTailwindRadiusTokens = ${literal(tailwindRadiusTokens)};`,
    `export const axisTailwindShadowTokens = ${literal(tailwindShadowTokens(dictionary))};`,
    `export const axisTailwindBackgroundImageTokens = ${literal(
      tailwindBackgroundImageTokens(dictionary),
    )};`,
  ];

  return `${sections.join('\n\n')}\n`;
}

StyleDictionary.registerFormat({
  name: 'axis/css-variables',
  format: cssVariables,
});

StyleDictionary.registerFormat({
  name: 'axis/typescript-registry',
  format: typescriptRegistry,
});

StyleDictionary.registerFormat({
  name: 'axis/tailwind-token-map',
  format: tailwindTokenMap,
});

StyleDictionary.registerTransform({
  name: 'axis/name-path',
  type: 'name',
  transform: (token) => token.path.join('-'),
});

